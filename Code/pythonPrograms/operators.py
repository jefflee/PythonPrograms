# Operators
# x + y | X加Y
# x - y | X減Y
# x * y | X乘Y
# x / y | X除以Y
# x // y | X除以Y，只取整數解
# x % y | 求X除以Y的餘數
# x ** y | X的Y次方

# Comparison operators
# x < y | X是否小於Y
# x <= y | X是否小於等於Y
# x > y | X是否大於Y
# x >= y | X是否大於等於Y
# x == y | X是否等於Y
# x != y | X是否不等於Y

# Boolean Operators
# a or b | A或B其中一個條件成立就回傳True
# a and b | A或B兩個條件都成立才回傳True
# not A | 如果A為True，則回傳False，反之則回傳True
