# variables and types
iv = 10
fv = 12.3
cv = 3 + 5j
sv = 'hello python'
bv = True
nv = None

print(iv, fv, cv, sv, bv)
print(type(iv))  # <class 'int'>
print(type(fv))  # <class 'float'>
print(type(cv))  # <class 'complex'>
print(type(sv))  # <class 'str'>
print(type(bv))  # <class 'bool'>
print(nv)  # None
print(isinstance(sv, str))  # True
